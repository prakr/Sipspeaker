package ik2213_sipspeaker;

/**
 *
 * @author Prakash
 */
public class MethodNotSupportedException extends Exception {

    /**
     * Creates a new instance of
     * <code>MethodNotSupportedException</code> without detail message.
     */
    public MethodNotSupportedException() {
    }

    /**
     * Constructs an instance of
     * <code>MethodNotSupportedException</code> with the specified detail
     * message.
     *
     * @param msg the detail message.
     */
    public MethodNotSupportedException(String msg) {
        super(msg);
    }
}
